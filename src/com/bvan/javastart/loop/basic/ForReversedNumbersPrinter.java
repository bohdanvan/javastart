package com.bvan.javastart.loop.basic;

/**
 * Output numbers from 40 to 30 with a for loop.
 *
 * @author bvanchuhov
 */
public class ForReversedNumbersPrinter {

    public static void main(String[] args) {
        for (int n = 40; n >= 30; n--) {
            System.out.println(n);
        }
    }
}
