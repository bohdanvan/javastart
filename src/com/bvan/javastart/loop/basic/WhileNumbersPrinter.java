package com.bvan.javastart.loop.basic;

/**
 * Output numbers from 10 to 20 with a while loop.
 *
 * @author bvanchuhov
 */
public class WhileNumbersPrinter {

    public static void main(String[] args) {
        int n = 10;
        while (n <= 20) {
            System.out.println(n);
            n++;
        }
    }
}
